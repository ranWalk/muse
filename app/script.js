const languageKey = "moonveil-language";
const getCurrentLanguage = () => localStorage.getItem(languageKey) || "en";

const applyLanguage = (language) => {
  const dictionary = translations[language] || translations.en;
  document.documentElement.lang = language === "zh" ? "zh-CN" : "en";
  document.title =
    language === "zh"
      ? document.title.replace("Private Contact", "私密咨询").replace("Intimate Wellness Essentials", "亲密健康独立站")
      : document.title.replace("私密咨询", "Private Contact").replace("亲密健康独立站", "Intimate Wellness Essentials");

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.dataset.i18n;
    if (dictionary[key]) {
      element.textContent = dictionary[key];
    }
  });

  document.querySelectorAll("[data-i18n-placeholder]").forEach((element) => {
    const key = element.dataset.i18nPlaceholder;
    if (dictionary[key]) {
      element.setAttribute("placeholder", dictionary[key]);
    }
  });

  document.querySelectorAll("[data-lang-switch]").forEach((button) => {
    button.classList.toggle("active", button.dataset.langSwitch === language);
  });
};

document.querySelectorAll("[data-lang-switch]").forEach((button) => {
  button.addEventListener("click", () => {
    localStorage.setItem(languageKey, button.dataset.langSwitch);
    applyLanguage(button.dataset.langSwitch);
    renderMessages();
  });
});

const messageForm = document.querySelector("[data-message-form]");
const messageBoard = document.querySelector("[data-message-board]");
const storageKey = "moonveil-private-messages";

const escapeHtml = (value) =>
  value
    .replaceAll("&", "&amp;")
    .replaceAll("<", "&lt;")
    .replaceAll(">", "&gt;")
    .replaceAll('"', "&quot;")
    .replaceAll("'", "&#39;");

const formatDate = (timestamp) =>
  new Date(timestamp).toLocaleString(getCurrentLanguage() === "zh" ? "zh-CN" : "en-US", {
    year: "numeric",
    month: getCurrentLanguage() === "zh" ? "2-digit" : "short",
    day: "2-digit",
    hour: "2-digit",
    minute: "2-digit",
  });

const loadMessages = () => {
  try {
    return JSON.parse(window.localStorage.getItem(storageKey) || "[]");
  } catch {
    return [];
  }
};

const saveMessages = (messages) => {
  window.localStorage.setItem(storageKey, JSON.stringify(messages));
};

const renderMessages = () => {
  if (!messageBoard) {
    return;
  }

  const dictionary = translations[getCurrentLanguage()] || translations.en;
  const messages = loadMessages();

  if (!messages.length) {
    messageBoard.innerHTML = `
      <article class="message-empty">
        <h3>${dictionary.emptyTitle}</h3>
        <p>${dictionary.emptyText}</p>
      </article>
    `;
    return;
  }

  messageBoard.innerHTML = messages
    .map(
      (item) => `
        <article class="message-card">
          <header>
            <strong>${escapeHtml(item.name)}</strong>
            <span class="message-meta">${formatDate(item.createdAt)}</span>
          </header>
          <div class="message-meta">${escapeHtml(item.contact || dictionary.noContact)}</div>
          <p>${escapeHtml(item.message)}</p>
        </article>
      `
    )
    .join("");
};

applyLanguage(getCurrentLanguage());

if (messageForm) {
  renderMessages();

  messageForm.addEventListener("submit", (event) => {
    event.preventDefault();

    const formData = new FormData(messageForm);
    const payload = {
      name: String(formData.get("name") || "").trim(),
      contact: String(formData.get("contact") || "").trim(),
      message: String(formData.get("message") || "").trim(),
      createdAt: Date.now(),
    };

    if (!payload.name || !payload.message) {
      return;
    }

    const messages = loadMessages();
    messages.unshift(payload);
    saveMessages(messages.slice(0, 20));
    messageForm.reset();
    renderMessages();
  });
}
